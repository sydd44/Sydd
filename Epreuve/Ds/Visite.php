									<h2>L'instruction au foyer</h2>									
									<p>Cela fait déjà quelques temps que tu es entrée dans la société de secours, alors je suppose que tu dois être plutôt habituée aux visites au foyer, en l'occurence les instructrices visiteuse pour vous les soeurs.</br>Mais as-tu vraiment conscience des responsabilités qu'implique ces simples "visites" ? Je t'invite à lire un peu ce qui suit, et à méditer sur le rôle de ces visites (tout en recherchant le pseudo du niveau... :P)</p>
									<p>Pour commencer tu peux lire <a href="https://www.lds.org/callings/relief-society/getting-started/roles-and-responsibilities/visiting-teacher?lang=fra" target="_blank">cette page</a>, j'aime notamment beaucoup le fais qu'en tant qu'instructeurs nous devons tous prendre soin les uns des autres, et pas uniquement de ceux qui nous sont plus particulièrement confié. Je pense que lorsque nous effectuons nos visites avec l'Esprit, c'est un peu comme si le Christ lui-même venait, et quelles bénédictions attendent nos familles ainsi que nous lorsque les choses se passent ainsi.</p>
									<p>La question qui reste alors est... Comment faire nos visites avec l'Esprit ? Biensûr chacun à sa propre relation avec ce dernier, cependant je pense que les points suivants aide grandement à l'inviter dans les foyers que nous visitons.</br>Tout d'abord une visite se prépare, cela signifie que l'on dois prendre un peu de notre temps avant de nous rendre chez nos familles. Durant ce temps il est plus que profitables de prier pour nos familles, et afin d'être receptif aux directions de l'Esprit.</br>Il faut également prendre le temps de méditer sur le sujet des visites que ce soit celui du <a href="../../pageniveau.php">Liahona</a>, ou celui qui nous est indiqué par l'Esprit. Durant cette méditation il faut essayer d'adapter le sujet a nos familles, afin de ne pas les blesser, mais de les fortifier.</br>Il faut de plus essayer d'échanger avec notre équipier afin que les deux soient sur la même longueur d'onde quant au message à donner.</br>Enfin, durant la visite il faut faire preuve d'abnégation, afin de ne pas la précipiter. Après tout si le Christ était physiquement présent, cela serait un peu déplacer de penser à sa machine de linge...</p>
									<p>Pour finir, j'aimerais insiter sur deux derniers points vitaux à mon sens, il s'agit premièrement de l'autre sens. Après tout notre rôle de soeurs visiteuses ne peux pas être magnifier si nous ne recevons pas avec amour celles qui doivent nous visiter ! </br>Deuxièmement, nos visites ne doivent pas s'arrêter à la visite que nous effectuons mensuellement, nous devons essayer de penser régulièrement à nos familles. Mais également, afin que nos visites soient magnifiées il faut également que nous faissions notre rapport. Dans les écritures on voit que le Christ demandera à chacun de nous un rapport de notre ministère. Le faire dèjà lors de cette vie est une excellente préparation pour ce jour grand et glorieux.</p>
