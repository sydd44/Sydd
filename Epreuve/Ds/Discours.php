									<h2>Les Discours</h2>	
									<span class="image left"><img src="../../images/Discours.jpg" alt="" /></span>								
									<p>Dans l'église nous avons la chance de nous enseigner les uns les autres, et il faut que cela se fasse à la façon du Seigneur, comme on peut le lire dans <a href="https://www.lds.org/scriptures/dc-testament/dc/88.122?lang=fra" target="_blank">D&A 88 : 122</a>.
									</br>Il est vrai qu'il faut suivre l'Esprit lorsque l'on prépare un discours, cependant, une bonne structure avec une introduction, un developpement et une conclusion est plus qu'important. Tous les discours d'autorité générale sont fait ainsi! Il y a de plus certaines autre choses que l'on retrouve plus ou moins systématiquement, que je vais lister :</p>
									<ul>
										<li>- Une figure imagée, telle qu'une parabole, une métaphore ou une simple comparaison.</li>
										<li>- Des écritures, ainsi que des citations plus ou moins longue d'autorité générale.</li>
										<li>- Une expérience de préférence personnelle, ou à défault une histoire.</li>
										<li>- Un témoignage personnel sur un thème plus ou moins proche de celui du discours.</li>
									</ul>
									<p>Il y a également des choses à éviter au plus haut point, parce que cela fait perdre l'attention de ceux qui écoute... A savoir :</p>
									<ul>
										<li>- Parler de la façon dont on nous a demandé de faire le discours.</li>
										<li>- Dire combien on a eu du mal, ou combien on a peur de faire ce discours.</li>
										<li>- Parler avec d'autre personne de quelque chose qui ne concerne pas les autres.</li>
										<li>- Vouloir traiter tout un sujet au complet (l'apprentissage de l'évangile prends tout une vie!)</li>
									</ul>
									<p>Pour voir si tu as parfaitement compris, je te demande de préparer un discours sur le thème de l'Expiation. Le sujet est vaste, ce qui te permettras de pouvoir tout de même cibler plus particulièrement un aspect qui te touche. </br> Bon courage! :-)</p>
