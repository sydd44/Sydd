									<h2>La Dîme</h2>									
									<p>La dîme est un principe fondemmental de l'évangile, le Seigneur l'a donné comme "loi préparatoire" aux membres, comme on peut le lire dans <a href="https://www.lds.org/scriptures/dc-testament/dc/119.1-7?lang=fra" target="_blank">D&A 119</a>. C'est intéressant de voir que le Seingeur dit qu'il s'agit d'une loi préparatoire, et éternelle... En faite c'est une loi éternelle, car elle vient de l'Eternel notre Dieu, mais son objectif est de nous préparer à la loi de consécration.</p>
									<p>J'aimerai que tu lises <a href="https://www.lds.org/scriptures/dc-testament/dc/104.13-14?lang=fra" target="_blank">cette écriture</a> ensuite, médites sur ce la façon dont ut dois utiliser tes biens qui enfaite sont les biens... De Dieu lui-même! A vrai dire lorsque l'on lit cela, je pense que quand le Seigneur dit <b>tout</b> cela veut dire <b>TOUT</b>, pas seulement notre argent, mais aussi et surtout notamment notre temps, et nos talent.</p>
									<p>Aujourd'hui lorsque nous payons notre dîme nous devons nous efforcer de réfléchir pour savoir si ce dixième qui symbolise ce que nous sommes prêt à donner au Seigneur est suffisamment "lourd". Pour cela je t'encourage à regarder cette histoire : </p>
									<iframe class="image right"width="560" height="315" src="https://www.youtube.com/embed/9luw-o5jwoM" frameborder="0" allowfullscreen></iframe>
									<p>Enfin, pour conclure sur ce point, j'aimerais que tu t'imagines plus tard devant ton Père céleste, et qu'Il te demande ce que tu as fais des biens qu'Il t'a confié. Et bien il faut que tu ais cette attitude lorsque tu va rentrer dans le bureau de l'êque et qu'il te posera la même question. Ce n'est qu'en étant honnête lors de cet entretien que l'on paie <b>complètement</b> sa dîme !</p>
