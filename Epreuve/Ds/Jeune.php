									<h2>Le Jeûne</h2>									
									<p> Le jeûne est quelque chose de très important dans l'évangile, et j'espère que tu pourras comprendre à quel point durant ce mois-ci. La première façon de comprendre l'importance du jeûne... </br>Et oui, c'est bien évidemment de le pratiquer ! Ainsi je veux que tu jeûne 2 fois ce mois-ci (1 fois avec la paroisse et 1 fois de manière personnel), à durant les deux essaie de pensé à quelque chose que tu aimerai changer, que tu ne comprends pas, et également à quelqu'un en particulier. </p>
									<p>De plus je souhaiterait que tu médites sur quelques écritures durant ce mois-ci. Et j'en profites pour te livrer ce que j'ai découvert sur le jeûne... Tout d'abord il faut comprendre le lien entre le corps et l'esprit, qui ensemble forment l'âme de l'homme, le corps est un élément indispensable pour obtenir l'exaltation c'est pour cela que nous avons besoin de venir sur terre pour expérimenté de nouvelle chose. Notamment les expériences spirituels ont une dimension bien physique, entrainant différentes sensations suivant chacun (coeur qui brûle, frisson, etc...). Cependant, le corps apporte, en même temps que toutes ces superbes émotions, des désirs et des passions naturels et non mauvais comme la procréation par exemple. Cependant chaque appétits à des raisons d'être et il y a une mesure en toute chose, c'est ainsi qu'il nous faut apprendre à controler nos appétits, en l'occurence la faim et la soif. (voir <i>Marc</i> 14 : 38) </p>
									<p>En privant notre corps physique de nourriture et de boisson, nous offrons à notre espit plus de force. L'exemple parfait en toute chose, le Christ, nous as montré cela dans les écriture, lorsque suite à son bâptème Il s'en alla dans le désert pour jeûner durant 40 jours et 40 nuits (tu remarquera à quel point j'ai été gentil avec toi :P). Je suis persuadé que c'est durant ce temps, pendant de nombreuses prières et de longues périodes de méditation qu'Il a découvert son identité, ainsi que sa mission sur terre, et oui, le Christ aussi est passé par le voile et n'avait donc aucun souvenir. Ensuite le diable est venu le tenté pensant que l'affaiblissement du corps du Sauveur le rendrait plus vulnérable, cependant son esprit lui avait-été fortifié, et Il ne pouvait plus douter qu'Il était le Fils de Dieu et le messie promis par les anciens prophètes depuis si longtemps (voir <i>Luc</i> 3). De même je peux te promettre que lorsque tu jeûnera, tu acquerras une force spirituelle bien plus grande, qui te permettras notamment de surmonter toutes sortes de difficultés ou de peines. </p>
									<p>Enfin un jour mon frère ma fait réfléchir sur le fait que lorsque l'on jeûne, nous pouvons travailler notre compassion, ainsi que notre charité. En effet pendant un peu de temps nous éprouvons les sensations de faim et de soif que d'autres ressentent parfois à chaque instant, mais pour nous c'est encore plus facile car nous savons qu'après la prière qui clôturera le jeûne, nous mangerons et boirons à notre faim. Mais nous pouvons faire un effort de plus, en donnant avec bon coeur une offrande de jeûne. </p>
									<p> Pour terminer je souhaiterais simplement t'inviter à lire (ou écouter) <a href="https://www.lds.org/general-conference/2015/04/is-not-this-the-fast-that-i-have-chosen?lang=fra" target="_blank">ce discours</a> par Henry B. Eyring sur le jeûne, ainsi que<a href="https://www.lds.org/general-conference/2001/04/the-law-of-the-fast?lang=fra" target="_blank"> celui-ci</a> de Joseph B. Wirthlin. J'aimerai également que tu médites sur les écritures suivantes en rapport avec le jeûne:</p>
									
