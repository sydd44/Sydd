									<header>
										<h2>J'imagine que tu es curieuse de savoir ce qui t'attend...</h2>
									</header>
									<span class="image featured"><img src="images/curieuse.jpg" alt="" /></span>
									<h3>Aller le moment est venu de tout te dévoiler (ou presque :-))</h3>

									<p>Je suis persuadé que tu te pose beaucoup de question sur ce qui t'attend,
									 et certaines réponses vont enfin arriver. Tu va enfin comprendre nombres de choses
									  que je te cachais avant de partir, et pour lesquelles tu ne manquais pas de curiosité ! Pendant au moins 2 ans nous n'allons pas nous voir... Et j'ai trouvé cette façon de m'assurer
									 que tu ne m'oubliera pas durant tout ce temps ! De plus il me fallait faire en sorte que
									  tu n'es pas beaucoup de temps libre, afin de ne pas passer trop de temps avec d'autres garçons :-P </p>

									 <p> De plus ce site sera pour moi l'occasion de t'aider à dévelloper certaine qualité, malgré mon absence.
									  J'ai considéré 6 catégories, que je ne te dévoile pas pour le moment, mais que tu auras bien l'occasion d'approfondir.
									   Ne t'en fais pas, parmis un tas d'activités studieuses se trouvent d'autres plus... "récréatives", tu me connais bien ;-)
									    Prépare toi à vivre des expériences intenses, parfois inattendues, mais toujours édifiantes ! </p>

									<h3>Concrètement... comment les choses vont-elles se dérouler ?</h3>

									<p>Chaque mois le site évoluera pour te proposer de nouvelles actions à réaliser dans chacun des 6 différents domaines. En faite le site est contruit en niveaux,
									au début les niveaux comportent 1 activité (je ne voudrais pas t'empêcher de réussir ton BAC !), puis 2 et au bout d'un moment 3, etc... Tu auras donc compris qu'il y a en tout 24 niveaux.</p>

									<p>Attention cependant, pour passer d'un niveaux à l'autre il te faudra à chaque fois un <strong>pseudo</strong> 
										et un <strong> mot de passe</strong>, qui seront bien entendu différents pour tous les niveaux. Le pseudo sera <strong>toujours </strong>
										composé exclusivement de lettres minuscules et sans aucun caractère spécial (ni accent ni apostrophe ni espace ni majuscule). Le mot de passe lui sera <strong>toujours </strong>
										compoé exclusivement de chiffres positifs (ni virgules ni points), plus simplement il s'agit d'entiers naturels (pour parler dans mon language ^^).
										J'insiste sur le fait que rentrer le bon pseudo et le bon mot de passe trop "tôt" n'auras <strong>aucun effet</strong>. Il te faudra à chaque fois attendre le
									 	1er du mois pour poursuivre, ainsi je suis sûr que tu mettra au moins 2 ans à "finir" le site.</p>

									<h3>Quelques petites règles à respecter tout de même...</h3>

									<p>Premièrement, le site est prévu spécialement pour être totalement autonôme. Inutile donc de me parler du site pour avoir des informations,
									 je l'ai pensé de manière à ne pas avoir à y retoucher en mission, j'aurais	bien plus important à faire là où je serais. 
									 De plus j'espère que nous aurons l'occasion d'en discuter plus tard, lorsque nous aurons la chance d'être de nouveau réuni <a class="icon fa-heart-o" title="Je T'aime!"></a></p>

									<p>Deuxièmement, tu n'es limité par rien, tu à <strong>carte blanche</strong> pour utiliser toutes les ressources dont tu disposes, notamment tu peux demander l'aide 
										de ta famille ou d'amis (je pense particulièrement à Google :P). En plus, pour les mois où trouver le pseudo et le mot de passe m'a semblé
										plus difficile j'ai prévu des "indices" qui aparaitrons sur le site au fur et à mesure. Soit le 15 de chaque mois s'il n'y a qu'un indice prévu, soit
										le 10 et le 20 dans le cas ou j'ai prévu 2 indices.</p>

									<p>Troisièmement, j'ai également prévu le cas de figure où toi tu partirais en mission ! Deux choix s'offriront alors à toi, le premier tu peux tout à fait
										ne rien changer et arrêter de consulter le site durant ta mission puis reprendre là où tu en étais rendu à ton retour (de plus normalement je serais également à tes côtés à ce moment :-)).
										Sinon, tu peux rentrer dans le champs pseudo : <strong>mission</strong> et dans le champs mot de passe : <strong>123456789</strong>, cela auras pour effet de suprimer la restriction chronologique, 
										tu pourras alors "enchainer" les niveaux aussi vite que tu peux. Si tu pars seulement 3 à 4 mois trop tôt je pense que c'est possible, en revanche si tu pars
										alors qu'il reste plus de 5 niveaux sur le site je pense que tu ferais mieux de faire une pause (les niveaux étant de plus en plus durs...). Enfin je compte sur toi
										pour ne pas utiliser cela afin d'aller plus vite, c'est <strong>uniquement </strong> dans le cas de ton départ en mission !</p>

									<p>Quatrièmement, pour le reste je t'expliquerai au fur et à mesure, au moment où tu en auras besoin ! Et à présent, je te souhaite de
										bien t'amuser, et d'en profiter un maximum pour t'améliorer encore et toujours :-) Je t'embrasse fort ma chérie, et j'espère que durant ces deux ans
										tu n'oubliera pas ô combien... Je t'aime <a class="icon fa-heart"></a> </p>
